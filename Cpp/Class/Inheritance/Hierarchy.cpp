#include <StdStream/StdStream.h>
#include <StdTest/StdTest.h>
#include <Stl.h>

/**************************************************************************************************
*   Members of class
*
**************************************************************************************************/

//-------------------------------------------------------------------------------------------------
struct Value1
{
	 Value1() { STD_TRACE_FUNC; }
	~Value1() { STD_TRACE_FUNC; }
};
//--------------------------------------------------------------------------------------------------
struct Value2
{
	 Value2() { STD_TRACE_FUNC; }
	~Value2() { STD_TRACE_FUNC; }
};
//--------------------------------------------------------------------------------------------------


/**************************************************************************************************
*   A classes
*
**************************************************************************************************/

//--------------------------------------------------------------------------------------------------
struct A
{
    A() { STD_TRACE_FUNC; }
};

struct A_Impl :
    A
{
    A_Impl() { STD_TRACE_FUNC; std::cout << std::endl; }
};
//--------------------------------------------------------------------------------------------------


/**************************************************************************************************
*   B classes
*
**************************************************************************************************/

//--------------------------------------------------------------------------------------------------
struct B
{
    B() { STD_TRACE_FUNC; }
};

struct C
{
    C() { STD_TRACE_FUNC; }
};

struct B_Impl :
    B,
    C
{
	B_Impl() { STD_TRACE_FUNC; std::cout << std::endl; }
};
//--------------------------------------------------------------------------------------------------


/**************************************************************************************************
*   Final class
*
**************************************************************************************************/

//--------------------------------------------------------------------------------------------------
struct Final final :
	A_Impl,
	B_Impl
{
	Value1 value1;
	Value2 value2;

	Final() { STD_TRACE_FUNC; }
   ~Final() { STD_TRACE_FUNC; }
};
//--------------------------------------------------------------------------------------------------
