/**************************************************************************************************
*   Member of class
*
**************************************************************************************************/

//-------------------------------------------------------------------------------------------------
struct Value
{
	 Value() { STD_TRACE_FUNC; }
	~Value() { STD_TRACE_FUNC; }
};
//--------------------------------------------------------------------------------------------------


/**************************************************************************************************
*   A classes
*
**************************************************************************************************/

//--------------------------------------------------------------------------------------------------
struct A1
{
    A1() { STD_TRACE_FUNC; }
};

struct A2 :
    A1
{
    A2() { STD_TRACE_FUNC; }
};

struct A3 :
    A2
{
    A3() { STD_TRACE_FUNC; std::cout << std::endl; }
};
//--------------------------------------------------------------------------------------------------


/**************************************************************************************************
*   B classes
*
**************************************************************************************************/

//--------------------------------------------------------------------------------------------------
struct B1
{
    B1() { STD_TRACE_FUNC; }
};

struct C1
{
    C1() { STD_TRACE_FUNC; }
};

struct B2 :
    B1,
    C1
{
    B2() { STD_TRACE_FUNC; std::cout << std::endl; }
};
//--------------------------------------------------------------------------------------------------


/**************************************************************************************************
*   Final class
*
**************************************************************************************************/

//--------------------------------------------------------------------------------------------------
struct Final final :
    A3,
    B2
{
	Value value;

	Final() { STD_TRACE_FUNC; }
   ~Final() { STD_TRACE_FUNC; }
};
