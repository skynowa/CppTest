struct Value
{
	 Value() { STD_TRACE_FUNC; }
	~Value() { STD_TRACE_FUNC; }
};
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
struct A1
{
    A1() { STD_TRACE_FUNC; }
};

struct A2 :
    A1
{
    A2() { STD_TRACE_FUNC; }
};

struct A3 :
    A2
{
    A3() { STD_TRACE_FUNC; std::cout << std::endl; }
};
//--------------------------------------------------------------------------------------------------
struct B1
{
    B1() { STD_TRACE_FUNC; }
};

struct C1
{
    C1() { STD_TRACE_FUNC; }
};

struct B2 :
    B1,
    C1
{
    B2() { STD_TRACE_FUNC; }
};

struct B3 :
    B2
{
    B3() { STD_TRACE_FUNC; std::cout << std::endl; }
};
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
struct Z final :
    A3,
    B3
{
	Value value;

    Z() { STD_TRACE_FUNC; }
};
