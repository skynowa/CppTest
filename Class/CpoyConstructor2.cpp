//---------------------------------------------------------------------------
#include <string>
#include <iostream>
#include <assert.h>
//------------------------------------------------------------------------------
class A
{
public:
         A();
    void show();

private:
    int         i;
    std::string s;
};
//------------------------------------------------------------------------------
A::A() :
    i(-1),
    s("string")
{
}
//------------------------------------------------------------------------------
void A::show()
{
    std::cout << "i = " << i << " "
              << "s = " << s << std::endl;
}
//---------------------------------------------------------------------------
int main(int, char **)
{
    A a1;
    A a2 = a1;

    a1.show();
    a2.show();

    return EXIT_SUCCESS;
}
