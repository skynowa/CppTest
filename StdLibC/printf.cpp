bool
bLogAppend(const char *pcszFilePath, const char *pcszText)
{
	assert(NULL != pcszFilePath);
	assert(NULL != pcszText);

	setlocale(LC_ALL, "");

	FILE *pFile = fopen(pcszFilePath, "a");
	assert(NULL != pFile);

	fprintf(pFile, "%s", pcszText);

	fclose(pFile);
	pFile = NULL;

	return true;
}
